fun main() {

    /*
    * Somar         (+)
    * Subtrair      (-)
    * Multiplicar   (*)
    * Dividir       (/)
    * Módulo        (%)
    * */

    var n1 = 11
    var n2 = 2
    var resSoma = n1 + n2
    var resSub = n1 - n2
    var resMult = n1 * n2
    var resDiv = n1 / n2
    var resMod = n1 % n2

    println("Soma = $resSoma")
    println("Subtração = $resSub")
    println("Multiplicação = $resMult")
    println("Divisão = $resDiv")
    println("Módulo = $resMod")
}